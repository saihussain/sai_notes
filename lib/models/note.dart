class Note {

  String title;

  String content;

  String date;

  bool pinned;

  bool favorite;

  bool archived;

  bool checklist;

  int color;

  String folder;

  String notebook;

  Note({

    required this.title,

    required this.content,

    required this.date,

    required this.pinned,

    required this.favorite,

    required this.archived,

    required this.checklist,

    required this.color,

    required this.folder,

    required this.notebook,
  });
}